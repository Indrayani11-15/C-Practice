//Write a program that keeps track of tea orders. Each time a cup of teais made decrease the number of cups remaining.The loop should run until all cups are served.
 #include<iostream>
 #include<string>
 using namespace std;
 int main(){
    int teaCups;
    cout<<"enter the number of tea cups to serve: ";
    cin>>teaCups;

    while (teaCups>0)
    {
        cout<<"Serving cup of tea \n"<<teaCups<<"remaining"<<endl;
        teaCups--;
    }
    cout<<"All tea cups are served"<<endl;
    

 }


